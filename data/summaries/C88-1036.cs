Metonymy has recently become a topic of interest in natural language processing (e.g., Grosz, Appelt, Martin, Pereira, and Shinkle 1985; Martin 1986; Hobbs and Martin 1987;.Stallard 1987; Wilensky 1987) though, to our knowledge, no one else has yet produced a theory embodied in a computer program that can process several different metonymic concepts or that can distinguish metonymies from metaphors.
